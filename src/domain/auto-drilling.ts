function moveTowardCenter(value: number, center: number, distance: number) {
  return value <= center
    ? Math.min(center, value + distance)
    : Math.max(center, value - distance);
}

function createBackPanelMinifixDowelAnchor(part: Part, face: PartFace, anchor: JoineryDebugAnchor, distance: number) {
  if (face === 'top' || face === 'bottom') {
    return {
      ...anchor,
      y: moveTowardCenter(anchor.y, part.thickness / 2, distance),
    };
  }
  if (face === 'left' || face === 'right') {
    return {
      ...anchor,
      x: moveTowardCenter(anchor.x, part.thickness / 2, distance),
    };
  }
  return anchor;
}

function shiftRafixMateTowardCenter(part: Part, face: PartFace, anchor: JoineryDebugAnchor) {
  if (face === 'top' || face === 'bottom') {
    return {
      ...anchor,
      y: Math.max(0, Math.min(part.thickness, anchor.y - RAFIX_MATE_CENTER_SHIFT)),
    };
  }
  if (face === 'left' || face === 'right') {
    return {
      ...anchor,
      x: Math.max(0, Math.min(part.thickness, anchor.x - RAFIX_MATE_CENTER_SHIFT)),
    };
  }
  return anchor;
}

