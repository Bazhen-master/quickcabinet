export function applyGeneratedJoinery(parts: Part[], rules: AutoJointRuleDraft = getDefaultAutoJointRules()): Part[] {
  const previousParts = parts;
  const cleaned = parts.map(withoutGeneratedOps);
  const updates = new Map<string, DrillOperation[]>();
  const byGroup = new Map<string, Part[]>();
  const shelfDebugDecisions: ShelfSidePipelineStat[] = [];

  cleaned.forEach((part) => {
    const groupId = part.meta?.groupId;
    if (!groupId) return;
    byGroup.set(groupId, [...(byGroup.get(groupId) ?? []), part]);
  });

  byGroup.forEach((groupParts) => {
    const leftSide = groupParts.find((part) => part.meta?.role === 'left-side');
    const rightSide = groupParts.find((part) => part.meta?.role === 'right-side');
    const top = groupParts.find((part) => part.meta?.role === 'top');
    const bottom = groupParts.find((part) => part.meta?.role === 'bottom');
    const backPanels = groupParts.filter((part) => part.meta?.role === 'back-panel' && !isSectionBackPanel(part.meta?.sourceId));
    const sectionBackPanels = groupParts.filter((part) => part.meta?.role === 'back-panel' && isSectionBackPanel(part.meta?.sourceId));
    const layoutCarrier = groupParts.find((part) => part.meta?.role === 'left-side' && part.meta?.cabinetLayout);
    if (!leftSide || !rightSide || !top || !bottom || !layoutCarrier?.meta?.cabinetLayout) return;

    const resolvedTiers = resolveCabinetTierLayouts(layoutCarrier.meta.cabinetLayout, bottom.width, leftSide.width);
    const primaryResolved = resolvedTiers[0]?.resolved;
    if (!primaryResolved) return;
    const plinthEnabled = isPlinthEnabled(groupParts);
    const overlayTop = isOverlayTop(top, bottom);

    const topJoinery = top.meta?.joinery ?? createEmptySideJoinery();
    const partitionParts = groupParts.filter((part) => part.meta?.role === 'partition');
    const topTierPartitionY = partitionParts.length > 0 ? Math.max(...partitionParts.map((part) => part.position.y)) : null;
    const bottomTierPartitionY = partitionParts.length > 0 ? Math.min(...partitionParts.map((part) => part.position.y)) : null;
    const topTierPartitions = partitionParts.filter((part) => topTierPartitionY === null || Math.abs(part.position.y - topTierPartitionY) < 0.001);
    const bottomTierPartitions = partitionParts.filter((part) => bottomTierPartitionY === null || Math.abs(part.position.y - bottomTierPartitionY) < 0.001);
    const leftFronts = groupParts.filter((part) => part.meta?.role === 'front-left');
    const rightFronts = groupParts.filter((part) => part.meta?.role === 'front-right');

    const topSupports: SectionSupport[] = overlayTop
      ? [
          {
            supportPart: leftSide,
            sectionSide: 'left',
            supportFace: topJoinery.left === 'minifix-dowel' ? 'right' : 'top',
          },
          {
            supportPart: rightSide,
            sectionSide: 'right',
            supportFace: topJoinery.right === 'minifix-dowel' ? 'left' : 'top',
          },
          ...topTierPartitions
            .map((supportPart) => ({ supportPart, sectionSide: 'right' as const, supportFace: 'top' as const })),
        ]
      : plinthEnabled
        ? [
            { supportPart: leftSide, sectionSide: 'left', supportFace: 'right' },
            { supportPart: rightSide, sectionSide: 'right', supportFace: 'left' },
          ]
        : [
            { supportPart: leftSide, sectionSide: 'left', supportFace: 'right' },
            { supportPart: rightSide, sectionSide: 'right', supportFace: 'left' },
            ...topTierPartitions
              .map((supportPart) => ({ supportPart, sectionSide: 'right' as const, supportFace: 'top' as const })),
          ];

    if (overlayTop) {
      topSupports.forEach((support) => {
        const sideKey = support.sectionSide;
        const ops = support.supportPart.meta?.role === 'partition'
          ? (() => {
              const joinery = getPartitionTopJoinery(support.supportPart, plinthEnabled);
              return createTopJoineryOps(top, support, `top-${support.supportPart.id}`, joinery, rules);
            })()
          : (() => {
              const joinery = sideKey === 'left' ? topJoinery.left : topJoinery.right;
              return joinery === 'minifix-dowel'
                ? createOverlayTopMinifixOps(top, support, rules)
                : joinery === 'confirmat'
                  ? createOverlayTopConfirmatOps(top, support.supportPart, sideKey, rules)
                  : { horizontalOps: [] as DrillOperation[], supportOps: [] as DrillOperation[] };
            })();
        appendOps(updates, top.id, ops.horizontalOps);
        appendOps(updates, support.supportPart.id, ops.supportOps);
      });
    } else {
      topSupports.forEach((support, idx) => {
        const sideKey = support.sectionSide === 'left' ? 'left' : 'right';
        const joinery =
          support.supportPart.meta?.role === 'left-side'
            ? topJoinery.left
            : support.supportPart.meta?.role === 'right-side'
              ? topJoinery.right
              : getPartitionTopJoinery(support.supportPart, plinthEnabled);
        const ops = createTopJoineryOps(top, support, support.supportPart.meta?.role === 'partition' ? `top-${idx}` : sideKey, joinery, rules);
        appendOps(updates, top.id, ops.horizontalOps);
        appendOps(updates, support.supportPart.id, ops.supportOps);
      });
    }

    const partitionsByX = new Map(
      partitionParts
        .map((part) => [part.position.x.toFixed(3), part] as const)
    );
      [...leftFronts, ...rightFronts].forEach((frontPart) => {
        const parsedSource = parseMainFrontSourceId(frontPart.meta?.sourceId);
        const side = frontPart.meta?.hingeEdge ?? parsedSource?.hingeSide ?? (frontPart.meta?.role === 'front-right' ? 'right' : 'left');
        const frontTierResolved = parsedSource?.tierId
          ? resolvedTiers.find((tier) => tier.tierId === parsedSource.tierId)?.resolved ?? primaryResolved
          : primaryResolved;
        const section = parsedSource?.sectionId
          ? frontTierResolved.leafSections.find((item) => item.id === parsedSource.sectionId) ?? null
          : frontTierResolved.leafSections
              .map((item) => ({ section: item, distance: Math.abs(item.centerX - (frontPart.position.x - top.position.x)) }))
              .sort((a, b) => a.distance - b.distance)[0]?.section ?? null;
        const tierPartitionsByX = parsedSource?.tierId
          ? new Map<string, Part>(
              (resolvedTiers.find((tier) => tier.tierId === parsedSource.tierId)?.resolved.partitions ?? [])
                .map((partition) => {
                  const supportPart = groupParts.find((part) => part.meta?.role === 'partition' && part.meta?.sourceId === partition.id);
                  return supportPart ? [partition.x.toFixed(3), supportPart] as const : null;
                })
                .filter((entry): entry is readonly [string, Part] => Boolean(entry))
            )
          : partitionsByX;
        const support = (side === 'left' || side === 'right') && section ? findSectionSupport(groupParts, section, side, tierPartitionsByX) : null;
      const supportPart =
        side === 'left'
          ? (support?.supportPart ?? leftSide)
          : side === 'right'
            ? (support?.supportPart ?? rightSide)
            : side === 'top'
              ? top
              : bottom;
      const supportFace =
        side === 'left'
          ? ((support?.supportFace as 'left' | 'right' | undefined) ?? 'right')
          : side === 'right'
            ? ((support?.supportFace as 'left' | 'right' | undefined) ?? 'left')
            : side === 'top'
              ? 'bottom'
              : 'top';
      const hingeOps = createFrontHingeOps(frontPart, supportPart, side, supportFace, groupParts);
      appendOps(updates, frontPart.id, hingeOps.frontOps);
      appendOps(updates, supportPart.id, hingeOps.supportOps);
    });

    backPanels.forEach((backPanel) => {
      const backPanelTopJoinery = getBackPanelEdgeJoinery(backPanel, 'top', getBackPanelTopJoinery(topJoinery, overlayTop));
      const topBackOps = backPanelTopJoinery === 'minifix-dowel'
        ? createBackPanelTopMinifixOps(top, backPanel, rules)
        : backPanelTopJoinery === 'rafix'
          ? createBackPanelTopRafixOps(top, backPanel, 'bottom')
        : backPanelTopJoinery === 'confirmat'
          ? createBackPanelTopConfirmatOps(top, backPanel)
          : { topOps: [] as DrillOperation[], backOps: [] as DrillOperation[] };
      appendOps(updates, top.id, 'topOps' in topBackOps ? topBackOps.topOps : topBackOps.hostOps);
      appendOps(updates, backPanel.id, topBackOps.backOps);

      const backPanelBottomJoinery = getBackPanelEdgeJoinery(backPanel, 'bottom');
      if (backPanelBottomJoinery === 'confirmat') {
        const bottomBackOps = createBackPanelBottomConfirmatOps(bottom, backPanel);
        appendOps(updates, bottom.id, bottomBackOps.bottomOps);
        appendOps(updates, backPanel.id, bottomBackOps.backOps);
      } else if (backPanelBottomJoinery === 'minifix-dowel') {
        const bottomBackOps = createBackPanelBottomMinifixOps(bottom, backPanel, rules);
        appendOps(updates, bottom.id, bottomBackOps.bottomOps);
        appendOps(updates, backPanel.id, bottomBackOps.backOps);
      } else if (backPanelBottomJoinery === 'rafix') {
        const bottomBackOps = createBackPanelBottomRafixOps(bottom, backPanel);
        appendOps(updates, bottom.id, bottomBackOps.hostOps);
        appendOps(updates, backPanel.id, bottomBackOps.backOps);
      }

      const panelMinX = backPanel.position.x - backPanel.width / 2;
      const panelMaxX = backPanel.position.x + backPanel.width / 2;

      if (panelMinX <= bottom.position.x - bottom.width / 2 + 0.001) {
        const backPanelLeftJoinery = getBackPanelEdgeJoinery(backPanel, 'left');
        if (backPanelLeftJoinery === 'confirmat') {
          const leftBackOps = createBackPanelSideConfirmatOps(backPanel, leftSide, 'left');
          appendOps(updates, backPanel.id, leftBackOps.backOps);
          appendOps(updates, leftSide.id, leftBackOps.sideOps);
        } else if (backPanelLeftJoinery === 'minifix-dowel') {
          const leftBackOps = createBackPanelSideMinifixOps(backPanel, leftSide, 'left', rules);
          appendOps(updates, backPanel.id, leftBackOps.backOps);
          appendOps(updates, leftSide.id, leftBackOps.sideOps);
        } else if (backPanelLeftJoinery === 'rafix') {
          const leftBackOps = createBackPanelSideRafixOps(backPanel, leftSide, 'left');
          appendOps(updates, backPanel.id, leftBackOps.backOps);
          appendOps(updates, leftSide.id, leftBackOps.sideOps);
        }
      }

      if (panelMaxX >= bottom.position.x + bottom.width / 2 - 0.001) {
        const backPanelRightJoinery = getBackPanelEdgeJoinery(backPanel, 'right');
        if (backPanelRightJoinery === 'confirmat') {
          const rightBackOps = createBackPanelSideConfirmatOps(backPanel, rightSide, 'right');
          appendOps(updates, backPanel.id, rightBackOps.backOps);
          appendOps(updates, rightSide.id, rightBackOps.sideOps);
        } else if (backPanelRightJoinery === 'minifix-dowel') {
          const rightBackOps = createBackPanelSideMinifixOps(backPanel, rightSide, 'right', rules);
          appendOps(updates, backPanel.id, rightBackOps.backOps);
          appendOps(updates, rightSide.id, rightBackOps.sideOps);
        } else if (backPanelRightJoinery === 'rafix') {
          const rightBackOps = createBackPanelSideRafixOps(backPanel, rightSide, 'right');
          appendOps(updates, backPanel.id, rightBackOps.backOps);
          appendOps(updates, rightSide.id, rightBackOps.sideOps);
        }
      }

    });

    sectionBackPanels.forEach((backPanel) => {
      const source = parseSectionBackPanelSource(backPanel.meta?.sourceId);
      if (!source) return;
      const tierIndex = resolvedTiers.findIndex((tier) => tier.tierId === source.tierId);
      if (tierIndex < 0) return;
      const tierResolved = resolvedTiers[tierIndex];
      const section = tierResolved?.resolved.leafSections.find((item) => item.id === source.sectionId);
      if (!section) return;

      const explicitTopJoinery = getBackPanelEdgeJoinery(backPanel, 'top', 'confirmat');
      const explicitBottomJoinery = getBackPanelEdgeJoinery(backPanel, 'bottom', 'confirmat');
      const explicitLeftJoinery = getBackPanelEdgeJoinery(backPanel, 'left', 'confirmat');
      const explicitRightJoinery = getBackPanelEdgeJoinery(backPanel, 'right', 'confirmat');

      if (explicitTopJoinery === 'confirmat' || explicitTopJoinery === 'rafix') {
        if (tierIndex === 0) {
          const topOps = explicitTopJoinery === 'rafix'
            ? createBackPanelTopRafixOps(top, backPanel, 'bottom')
            : createBackPanelTopConfirmatOps(top, backPanel);
          appendOps(updates, top.id, 'topOps' in topOps ? topOps.topOps : topOps.hostOps);
          appendOps(updates, backPanel.id, topOps.backOps);
        } else {
          const upperDivider = groupParts.find((part) => part.meta?.role === 'tier-divider' && part.meta?.sourceId === `tier-divider:${resolvedTiers[tierIndex - 1]?.tierId}`);
          if (upperDivider) {
            const topOps = explicitTopJoinery === 'rafix'
              ? createBackPanelTopRafixOps(upperDivider, backPanel, 'bottom')
              : createBackPanelTopToBottomFaceConfirmatOps(upperDivider, backPanel);
            appendOps(updates, upperDivider.id, topOps.hostOps);
            appendOps(updates, backPanel.id, topOps.backOps);
          }
        }
      }

      if (explicitBottomJoinery === 'confirmat' || explicitBottomJoinery === 'minifix-dowel' || explicitBottomJoinery === 'rafix') {
        if (tierIndex === resolvedTiers.length - 1) {
          const bottomOps = explicitBottomJoinery === 'rafix'
            ? createBackPanelBottomRafixOps(bottom, backPanel)
            : explicitBottomJoinery === 'minifix-dowel'
              ? createBackPanelBottomMinifixOps(bottom, backPanel, rules)
              : createBackPanelBottomConfirmatOps(bottom, backPanel);
          appendOps(updates, bottom.id, 'bottomOps' in bottomOps ? bottomOps.bottomOps : bottomOps.hostOps);
          appendOps(updates, backPanel.id, bottomOps.backOps);
        } else {
          const divider = groupParts.find((part) => part.meta?.role === 'tier-divider' && part.meta?.sourceId === `tier-divider:${source.tierId}`);
          if (divider) {
            const bottomOps = explicitBottomJoinery === 'rafix'
              ? createBackPanelBottomRafixOps(divider, backPanel)
              : explicitBottomJoinery === 'minifix-dowel'
                ? createBackPanelBottomMinifixOps(divider, backPanel, rules)
                : createBackPanelBottomConfirmatOps(divider, backPanel);
            appendOps(updates, divider.id, 'bottomOps' in bottomOps ? bottomOps.bottomOps : bottomOps.hostOps);
            appendOps(updates, backPanel.id, bottomOps.backOps);
          }
        }
      }

      if (explicitLeftJoinery === 'confirmat' || explicitLeftJoinery === 'minifix-dowel' || explicitLeftJoinery === 'rafix') {
        if (section.leftBoundary === 'outer') {
          const leftBackOps = explicitLeftJoinery === 'rafix'
            ? createBackPanelSideRafixOps(backPanel, leftSide, 'left')
            : explicitLeftJoinery === 'minifix-dowel'
              ? createBackPanelSideMinifixOps(backPanel, leftSide, 'left', rules)
              : createBackPanelSideConfirmatOps(backPanel, leftSide, 'left');
          appendOps(updates, backPanel.id, leftBackOps.backOps);
          appendOps(updates, leftSide.id, leftBackOps.sideOps);
        } else {
          const partition = tierResolved.resolved.partitions.find((item) => Math.abs(item.x - section.startX) < 0.001);
          const partitionPart = partition ? findPartitionPartBySourceId(groupParts, partition.id) : null;
          if (partitionPart) {
            const partitionBackOps = explicitLeftJoinery === 'rafix'
              ? createPartitionBackRafixOps(partitionPart, backPanel)
              : explicitLeftJoinery === 'minifix-dowel'
                ? createPartitionBackMinifixOps(partitionPart, backPanel, rules)
                : createPartitionBackConfirmatOps(partitionPart, backPanel);
            appendOps(updates, partitionPart.id, partitionBackOps.partitionOps);
            appendOps(updates, backPanel.id, partitionBackOps.backOps);
          }
        }
      }

      if (explicitRightJoinery === 'confirmat' || explicitRightJoinery === 'minifix-dowel' || explicitRightJoinery === 'rafix') {
        if (section.rightBoundary === 'outer') {
          const rightBackOps = explicitRightJoinery === 'rafix'
            ? createBackPanelSideRafixOps(backPanel, rightSide, 'right')
            : explicitRightJoinery === 'minifix-dowel'
              ? createBackPanelSideMinifixOps(backPanel, rightSide, 'right', rules)
              : createBackPanelSideConfirmatOps(backPanel, rightSide, 'right');
          appendOps(updates, backPanel.id, rightBackOps.backOps);
          appendOps(updates, rightSide.id, rightBackOps.sideOps);
        } else {
          const partition = tierResolved.resolved.partitions.find((item) => Math.abs(item.x - section.endX) < 0.001);
          const partitionPart = partition ? findPartitionPartBySourceId(groupParts, partition.id) : null;
          if (partitionPart) {
            const partitionBackOps = explicitRightJoinery === 'rafix'
              ? createPartitionBackRafixOps(partitionPart, backPanel)
              : explicitRightJoinery === 'minifix-dowel'
                ? createPartitionBackMinifixOps(partitionPart, backPanel, rules)
                : createPartitionBackConfirmatOps(partitionPart, backPanel);
            appendOps(updates, partitionPart.id, partitionBackOps.partitionOps);
            appendOps(updates, backPanel.id, partitionBackOps.backOps);
          }
        }
      }
    });

    groupParts
      .filter((part) => part.meta?.role === 'shelf' && part.meta?.sourceId)
      .forEach((shelfPart) => {
        const tier = resolvedTiers.find((item) => item.resolved.shelvesBySection.size > 0 && [...item.resolved.shelvesBySection.values()].flat().some((shelf) => shelf.id === shelfPart.meta?.sourceId));
        const section = tier?.resolved.leafSections.find((item) => (tier.resolved.shelvesBySection.get(item.id) ?? []).some((shelf) => shelf.id === shelfPart.meta?.sourceId));
        if (!section) return;
        const partitionsByX = new Map<string, Part>();
        tier?.resolved.partitions.forEach((partition) => {
          const supportPart = groupParts.find((part) => part.meta?.role === 'partition' && part.meta?.sourceId === partition.id);
          if (supportPart) partitionsByX.set(partition.x.toFixed(3), supportPart);
        });
        const expectedLeftSupportPartId = getExpectedSectionSupportPartId(section, 'left', leftSide, rightSide, partitionsByX);
        const expectedRightSupportPartId = getExpectedSectionSupportPartId(section, 'right', leftSide, rightSide, partitionsByX);
        const fallbackLeftSupport = findSectionSupport(groupParts, section, 'left', partitionsByX);
        const fallbackRightSupport = findSectionSupport(groupParts, section, 'right', partitionsByX);
        const explicitLeftSupport = resolveExplicitShelfSupport(groupParts, shelfPart, 'left');
        const explicitRightSupport = resolveExplicitShelfSupport(groupParts, shelfPart, 'right');
        const leftSupport = explicitLeftSupport ?? fallbackLeftSupport;
        const rightSupport = explicitRightSupport ?? fallbackRightSupport;
        const joinery = shelfPart.meta?.joinery ?? createEmptySideJoinery();
        const leftDecision = resolveShelfSideJoinery(shelfPart, section.id, 'left', joinery.left, leftSupport);
        const rightDecision = resolveShelfSideJoinery(shelfPart, section.id, 'right', joinery.right, rightSupport);

        [leftDecision, rightDecision].forEach((decision) => {
          const removedOldOps = decision.sourceKey ? countOpsBySource(previousParts, decision.sourceKey) : 0;
          const expectedSupportPartId = decision.side === 'left' ? expectedLeftSupportPartId : expectedRightSupportPartId;
          const explicitSupportPartId = decision.side === 'left' ? shelfPart.meta?.leftSupportPartId : shelfPart.meta?.rightSupportPartId;
          const hasExplicitSupport = Boolean(explicitSupportPartId);
          const explicitSupportMatchesSection = explicitSupportPartId && expectedSupportPartId
            ? explicitSupportPartId === expectedSupportPartId
            : true;
          const isJoineryAllowed =
            decision.joinery === 'none'
            || decision.joinery === 'confirmat'
            || decision.joinery === 'minifix-dowel'
            || decision.joinery === 'shelf_pin';
          const adjacencyValid = decision.support
            ? isValidShelfSupportAdjacency(shelfPart, decision.support)
            : false;

          if (!isJoineryAllowed) {
            console.warn('[shelf-joinery-debug][joinery-disabled-in-stage]', {
              stage: SHELF_JOINERY_REBUILD_STAGE,
              shelfId: decision.shelfId,
              parentSectionId: decision.parentSectionId,
              side: decision.side,
              joinery: decision.joinery,
            });
          }

          if (decision.joinery !== 'none' && !hasExplicitSupport) {
            console.warn('[shelf-joinery-debug][missing-explicit-shelf-support]', {
              stage: SHELF_JOINERY_REBUILD_STAGE,
              shelfId: decision.shelfId,
              parentSectionId: decision.parentSectionId,
              side: decision.side,
              joinery: decision.joinery,
              expectedSupportPartId,
            });
          }
          if (decision.joinery !== 'none' && hasExplicitSupport && expectedSupportPartId && !explicitSupportMatchesSection) {
            console.warn('[shelf-joinery-debug][support-not-in-parent-section]', {
              stage: SHELF_JOINERY_REBUILD_STAGE,
              shelfId: decision.shelfId,
              parentSectionId: decision.parentSectionId,
              side: decision.side,
              joinery: decision.joinery,
              supportPartId: decision.supportPartId,
              expectedSupportPartId,
            });
          }

          if (!decision.support || decision.joinery === 'none' || !isJoineryAllowed || !adjacencyValid) {
            if (decision.joinery !== 'none' && !decision.support) {
              console.warn('[shelf-joinery-debug][no-support]', {
                stage: SHELF_JOINERY_REBUILD_STAGE,
                shelfId: decision.shelfId,
                parentSectionId: decision.parentSectionId,
                side: decision.side,
                joinery: decision.joinery,
                sourceKey: decision.sourceKey,
              });
            }
            if (decision.joinery !== 'none' && decision.support && !adjacencyValid && explicitSupportMatchesSection) {
              console.warn('[shelf-joinery-debug][invalid-adjacency]', {
                shelfId: decision.shelfId,
                parentSectionId: decision.parentSectionId,
                side: decision.side,
                joinery: decision.joinery,
                supportPartId: decision.supportPartId,
                supportPartType: decision.supportPartType,
                sourceKey: decision.sourceKey,
              });
            }
            shelfDebugDecisions.push({
              ...decision,
              generatedOps: 0,
              removedOldOps,
              finalStoredOps: 0,
              renderedMarkers: 0,
              adjacencyValid,
            });
            return;
          }
          const ops = createShelfSideJoineryOps(shelfPart, decision.support, decision.side, decision.joinery, rules);
          const taggedHorizontalOps = tagShelfAutoGeneratedOps(ops.horizontalOps, decision, shelfPart);
          const taggedSupportOps = tagShelfAutoGeneratedOps(ops.supportOps, decision, decision.support.supportPart);
          appendOps(updates, shelfPart.id, taggedHorizontalOps);
          appendOps(updates, decision.support.supportPart.id, taggedSupportOps);
          shelfDebugDecisions.push({
            ...decision,
            generatedOps: taggedHorizontalOps.length + taggedSupportOps.length,
            removedOldOps,
            finalStoredOps: 0,
            renderedMarkers: 0,
            adjacencyValid,
          });
        });
      });

    groupParts
      .filter((part) => part.meta?.role === 'apron' && part.meta?.sourceId)
      .forEach((apronPart) => {
        const [hostRole, sectionId, hostSourceId] = (apronPart.meta?.sourceId ?? '').split(':');
        const tier = resolvedTiers.find((item) => item.resolved.leafSections.some((leaf) => leaf.id === sectionId));
        const section = tier?.resolved.leafSections.find((item) => item.id === sectionId);
        if (!section) return;
        const partitionsByX = new Map<string, Part>();
        tier?.resolved.partitions.forEach((partition) => {
          const supportPart = groupParts.find((part) => part.meta?.role === 'partition' && part.meta?.sourceId === partition.id);
          if (supportPart) partitionsByX.set(partition.x.toFixed(3), supportPart);
        });
        const leftSupport = findSectionSupport(groupParts, section, 'left', partitionsByX);
        const rightSupport = findSectionSupport(groupParts, section, 'right', partitionsByX);
        const hostPart =
          hostRole === 'top'
            ? top
            : hostRole === 'bottom'
              ? bottom
              : groupParts.find((part) => part.meta?.role === 'shelf' && part.meta?.sourceId === hostSourceId) ?? null;
        if (!hostPart) return;

        const apronHostJoinery = getApronHostJoinery(apronPart, hostRole);
        if (apronHostJoinery !== 'none') {
          const hostFace = hostRole === 'bottom' ? 'top' as const : 'bottom' as const;
          const apronFace = hostRole === 'bottom' ? 'bottom' as const : 'top' as const;
          const hostOps = apronHostJoinery === 'minifix-dowel'
            ? createApronToHostMinifixOps(apronPart, hostPart, `apron-host-${sectionId}`, hostFace, apronFace)
            : apronHostJoinery === 'rafix'
              ? createApronToHostRafixOps(apronPart, hostPart, `apron-host-${sectionId}`, hostFace, apronFace)
              : createApronToHostConfirmatOps(apronPart, hostPart, `apron-host-${sectionId}`, hostFace, apronFace);
          appendOps(updates, apronPart.id, hostOps.apronOps);
          appendOps(updates, hostPart.id, hostOps.hostOps);
        }

        if (leftSupport) {
          const leftJoinery = getApronSideJoinery(apronPart, 'left');
          if (leftJoinery !== 'none') {
            const ops = leftJoinery === 'minifix-dowel'
              ? createApronSideMinifixOps(apronPart, leftSupport, 'left')
              : leftJoinery === 'rafix'
                ? createApronSideRafixOps(apronPart, leftSupport, 'left')
                : createApronSideConfirmatOps(apronPart, leftSupport, 'left');
            appendOps(updates, apronPart.id, ops.apronOps);
            appendOps(updates, leftSupport.supportPart.id, ops.supportOps);
          }
        }
        if (rightSupport) {
          const rightJoinery = getApronSideJoinery(apronPart, 'right');
          if (rightJoinery !== 'none') {
            const ops = rightJoinery === 'minifix-dowel'
              ? createApronSideMinifixOps(apronPart, rightSupport, 'right')
              : rightJoinery === 'rafix'
                ? createApronSideRafixOps(apronPart, rightSupport, 'right')
                : createApronSideConfirmatOps(apronPart, rightSupport, 'right');
            appendOps(updates, apronPart.id, ops.apronOps);
            appendOps(updates, rightSupport.supportPart.id, ops.supportOps);
          }
        }
      });

    groupParts
      .filter((part) => part.meta?.role === 'tier-divider' && part.meta?.sourceId)
      .forEach((dividerPart) => {
        const dividerTierId = dividerPart.meta?.sourceId?.replace('tier-divider:', '') ?? '';
        const dividerTierIndex = resolvedTiers.findIndex((tier) => tier.tierId === dividerTierId);
        if (dividerTierIndex < 0) return;

        const dividerJoinery = dividerPart.meta?.joinery ?? createEmptySideJoinery();
        const upperTier = resolvedTiers[dividerTierIndex] ?? null;
        const lowerTier = resolvedTiers[dividerTierIndex + 1] ?? null;

        const leftDividerSupport: SectionSupport = { supportPart: leftSide, sectionSide: 'left', supportFace: 'right' };
        const rightDividerSupport: SectionSupport = { supportPart: rightSide, sectionSide: 'right', supportFace: 'left' };

        if (dividerJoinery.left !== 'none') {
          const ops = createHorizontalJoineryOps(dividerPart, leftDividerSupport, 'left', dividerJoinery.left, rules);
          appendOps(updates, dividerPart.id, ops.horizontalOps);
          appendOps(updates, leftSide.id, ops.supportOps);
        }

        if (dividerJoinery.right !== 'none') {
          const ops = createHorizontalJoineryOps(dividerPart, rightDividerSupport, 'right', dividerJoinery.right, rules);
          appendOps(updates, dividerPart.id, ops.horizontalOps);
          appendOps(updates, rightSide.id, ops.supportOps);
        }

        if (upperTier && dividerJoinery.top !== 'none') {
          upperTier.resolved.partitions.forEach((partition, idx) => {
            const supportPart = findPartitionPartBySourceId(groupParts, partition.id);
            if (!supportPart) return;
            const support: SectionSupport = {
              supportPart,
              sectionSide: 'right',
              supportFace: getPartitionTopCamFace(dividerPart, supportPart),
            };
            const ops = dividerJoinery.top === 'confirmat'
              ? createBottomPartitionConfirmatOps(dividerPart, support, `tier-divider-top-${idx}`, rules)
              : dividerJoinery.top === 'minifix-dowel'
                ? createBottomPartitionMinifixDowelOps(dividerPart, support, `tier-divider-top-${idx}`, rules)
                : dividerJoinery.top === 'rafix'
                  ? createBottomPartitionRafixOps(dividerPart, support, `tier-divider-top-${idx}`, rules)
                  : { horizontalOps: [] as DrillOperation[], supportOps: [] as DrillOperation[] };
            appendOps(updates, dividerPart.id, ops.horizontalOps);
            appendOps(updates, supportPart.id, ops.supportOps);
          });
        }

        if (lowerTier && dividerJoinery.bottom !== 'none') {
          lowerTier.resolved.partitions.forEach((partition, idx) => {
            const supportPart = findPartitionPartBySourceId(groupParts, partition.id);
            if (!supportPart) return;
            const support: SectionSupport = {
              supportPart,
              sectionSide: 'right',
              supportFace: 'top',
            };
            const ops = createTopJoineryOps(dividerPart, support, `tier-divider-bottom-${idx}`, dividerJoinery.bottom, rules);
            appendOps(updates, dividerPart.id, ops.horizontalOps);
            appendOps(updates, supportPart.id, ops.supportOps);
          });
        }
      });

    const bottomSupports: SectionSupport[] = [
      { supportPart: leftSide, sectionSide: 'left', supportFace: 'right' },
      ...bottomTierPartitions
        .map((supportPart) => ({ supportPart, sectionSide: 'right' as const, supportFace: 'left' as const })),
      { supportPart: rightSide, sectionSide: 'right', supportFace: 'left' },
    ];

    bottomSupports.forEach((support, idx) => {
      const sideKey = `bottom-${idx}`;
      const ops = support.supportPart.meta?.role === 'partition'
        ? (() => {
            const joinery = getPartitionBottomJoinery(support.supportPart, 'none');
            if (joinery === 'confirmat') return createBottomPartitionConfirmatOps(bottom, support, sideKey, rules);
            if (joinery === 'minifix-dowel') return createBottomPartitionMinifixDowelOps(bottom, support, sideKey, rules);
            if (joinery === 'rafix') return createBottomPartitionRafixOps(bottom, support, sideKey, rules);
            return { horizontalOps: [] as DrillOperation[], supportOps: [] as DrillOperation[] };
          })()
        : createHorizontalJoineryOps(bottom, support, sideKey, getBottomSideJoinery(bottom, support.sectionSide), rules);
      appendOps(updates, bottom.id, ops.horizontalOps);
      appendOps(updates, support.supportPart.id, ops.supportOps);
    });
  });

  const result = cleaned.map((part) =>
    normalizePartDrillOperations(
      updates.has(part.id)
        ? {
            ...part,
            operations: [...part.operations, ...(updates.get(part.id) ?? [])],
          }
        : part
    )
  );
  const finalizedShelfStats = shelfDebugDecisions.map((stat) => {
    const finalStoredOps = stat.sourceKey ? countOpsBySource(result, stat.sourceKey) : 0;
    const renderedMarkers = finalStoredOps;
    if (finalStoredOps > stat.generatedOps) {
      console.warn('[shelf-joinery-debug][stale-ops-detected]', {
        shelfId: stat.shelfId,
        side: stat.side,
        sourceKey: stat.sourceKey,
        generatedOps: stat.generatedOps,
        finalStoredOps,
      });
    }
    return { ...stat, finalStoredOps, renderedMarkers };
  });
  debugShelfJoineryDump(finalizedShelfStats);
  return result;
}
