function getCamDowelPairs(depth: number, rules: AutoJointRuleDraft) {
  const [frontCenter, backCenter] = clampOffsets(depth, rules.frontOffset, rules.backOffset);
  const halfSpacing = Math.max(8, rules.camDowelSpacing / 2);
  const clampX = (value: number) => Math.max(9, Math.min(depth - 9, value));
  const detailCenter = depth / 2;
  return [frontCenter, backCenter].map((center) => {
    const dowelTowardCenter = center <= detailCenter;
    return {
      camX: clampX(center + (dowelTowardCenter ? -halfSpacing : halfSpacing)),
      dowelX: clampX(center + (dowelTowardCenter ? halfSpacing : -halfSpacing)),
    };
  });
}

function getShelfCamDowelPairs(shelf: Part, rules: AutoJointRuleDraft, side: 'left' | 'right') {
  const centers = getShelfDepthOffsets(shelf, rules, side);
  const halfSpacing = Math.max(8, rules.camDowelSpacing / 2);
  const clampDepth = (value: number) => Math.max(9, Math.min(shelf.thickness - 9, value));
  const depthCenter = getShelfDrillReferenceDepth(shelf) / 2;
  return centers.map((center) => {
    const dowelTowardCenter = center <= depthCenter;
    return {
      camX: clampDepth(center + (dowelTowardCenter ? -halfSpacing : halfSpacing)),
      dowelX: clampDepth(center + (dowelTowardCenter ? halfSpacing : -halfSpacing)),
    };
  });
}

function getWorldAnchorPosition(part: Part, face: PartFace, anchor: JoineryDebugAnchor): WorldPoint {
  switch (face) {
    case 'front':
      return {
        x: part.position.x - part.width / 2 + anchor.x,
        y: part.position.y + part.height / 2 - anchor.y,
        z: part.position.z + part.thickness / 2,
      };
    case 'back':
      return {
        x: part.position.x - part.width / 2 + anchor.x,
        y: part.position.y + part.height / 2 - anchor.y,
        z: part.position.z - part.thickness / 2,
      };
    case 'top':
      return {
        x: part.position.x - part.width / 2 + anchor.x,
        y: part.position.y + part.height / 2,
        z: part.position.z + part.thickness / 2 - anchor.y,
      };
    case 'bottom':
      return {
        x: part.position.x - part.width / 2 + anchor.x,
        y: part.position.y - part.height / 2,
        z: part.position.z + part.thickness / 2 - anchor.y,
      };
    case 'left':
      return {
        x: part.position.x - part.width / 2,
        y: part.position.y + part.height / 2 - anchor.y,
        z: part.position.z + part.thickness / 2 - anchor.x,
      };
    case 'right':
      return {
        x: part.position.x + part.width / 2,
        y: part.position.y + part.height / 2 - anchor.y,
        z: part.position.z + part.thickness / 2 - anchor.x,
      };
  }
}

function projectWorldPointToFace(part: Part, face: PartFace, point: WorldPoint): JoineryDebugAnchor {
  switch (face) {
    case 'front':
    case 'back':
      return {
        x: Math.max(0, Math.min(part.width, point.x - (part.position.x - part.width / 2))),
        y: Math.max(0, Math.min(part.height, part.position.y + part.height / 2 - point.y)),
      };
    case 'top':
    case 'bottom':
      return {
        x: Math.max(0, Math.min(part.width, point.x - (part.position.x - part.width / 2))),
        y: Math.max(0, Math.min(part.thickness, part.position.z + part.thickness / 2 - point.z)),
      };
    case 'left':
    case 'right':
      return {
        x: Math.max(0, Math.min(part.thickness, part.position.z + part.thickness / 2 - point.z)),
        y: Math.max(0, Math.min(part.height, part.position.y + part.height / 2 - point.y)),
      };
  }
}

function getFaceMaxDepth(part: Part, face: PartFace) {
  switch (face) {
    case 'front':
    case 'back':
      return part.thickness;
    case 'top':
    case 'bottom':
      return part.height;
    case 'left':
    case 'right':
      return part.width;
  }
}

