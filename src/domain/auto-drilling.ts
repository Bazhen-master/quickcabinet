function countOpsBySource(parts: Part[], sourceKey: string) {
  return parts.reduce((count, part) => count + part.operations.filter((op) => op.source === sourceKey).length, 0);
}

function getSupportFaceWorldX(support: SectionSupport) {
  if (support.supportFace === 'left') return support.supportPart.position.x - support.supportPart.width / 2;
  if (support.supportFace === 'right') return support.supportPart.position.x + support.supportPart.width / 2;
  return support.supportPart.position.x;
}

function getExpectedSectionSupportPartId(
  section: CabinetSection,
  side: 'left' | 'right',
  leftSide: Part,
  rightSide: Part,
  partitionsByX: Map<string, Part>
) {
  if (side === 'left') {
    if (section.leftBoundary === 'outer') return leftSide.id;
    return partitionsByX.get(section.startX.toFixed(3))?.id ?? null;
  }
  if (section.rightBoundary === 'outer') return rightSide.id;
  return partitionsByX.get(section.endX.toFixed(3))?.id ?? null;
}

function resolveExplicitShelfSupport(
  groupParts: Part[],
  shelfPart: Part,
  side: 'left' | 'right'
): SectionSupport | null {
  const supportPartId = side === 'left' ? shelfPart.meta?.leftSupportPartId : shelfPart.meta?.rightSupportPartId;
  if (!supportPartId) return null;
  const supportPart = groupParts.find((part) => part.id === supportPartId) ?? null;
  if (!supportPart) return null;
  return {
    supportPart,
    sectionSide: side,
    supportFace: side === 'left' ? 'right' : 'left',
  };
}

function isValidShelfSupportAdjacency(shelfPart: Part, support: SectionSupport) {
  const role = support.supportPart.meta?.role;
  if (role !== 'left-side' && role !== 'right-side' && role !== 'partition') return false;
  const shelfFaceX = support.sectionSide === 'left'
    ? shelfPart.position.x - shelfPart.width / 2
    : shelfPart.position.x + shelfPart.width / 2;
  const supportFaceX = getSupportFaceWorldX(support);
  const xAligned = Math.abs(shelfFaceX - supportFaceX) <= 1;
  const supportMinY = support.supportPart.position.y - support.supportPart.height / 2 - 1;
  const supportMaxY = support.supportPart.position.y + support.supportPart.height / 2 + 1;
  const yAligned = shelfPart.position.y >= supportMinY && shelfPart.position.y <= supportMaxY;
  return xAligned && yAligned;
}

function tagShelfAutoGeneratedOps(
  ops: DrillOperation[],
  decision: ResolvedShelfSideJoinery,
  ownerPart: Part
) {
  if (!decision.sourceKey || !decision.supportPartId || !decision.supportPartType) return ops;
  const tag: AutoGeneratedDrillMeta = {
    isAutoGenerated: true,
    ownerPartId: ownerPart.id,
    ownerPartType: ownerPart.meta?.role ?? 'panel',
    shelfId: decision.shelfId,
    side: decision.side,
    supportPartId: decision.supportPartId,
    supportPartType: decision.supportPartType,
    joineryType: decision.joinery,
    sourceKey: decision.sourceKey,
  };
  return ops.map((op) => ({ ...op, autoGenerated: tag }));
}

function getHorizontalOffsets(part: Part, rules: AutoJointRuleDraft) {
  return clampOffsets(part.thickness, rules.frontOffset, rules.backOffset);
}

function getShelfDrillReferenceDepth(shelf: Part) {
  const value = shelf.meta?.shelfDrillReferenceDepth;
  return Number.isFinite(value) && (value ?? 0) > 0 ? (value as number) : shelf.thickness;
}

function getShelfDepthOffsets(shelf: Part, rules: AutoJointRuleDraft, side: 'left' | 'right') {
  const minDepth = 9;
  const maxDepth = Math.max(minDepth, shelf.thickness - 9);
  const clampDepth = (value: number) => Math.max(minDepth, Math.min(maxDepth, value));
  const referenceDepth = getShelfDrillReferenceDepth(shelf);
  const frontAnchor = clampDepth(rules.frontOffset);
  const backAnchor = clampDepth(referenceDepth - rules.backOffset);
  return [frontAnchor, backAnchor]
    .map((offset) => clampDepth(offset))
    .filter((value, index, array) => array.findIndex((item) => Math.abs(item - value) < 0.001) === index);
}

function getHorizontalSideLocalX(horizontalPart: Part, side: 'left' | 'right', offset: number) {
  const clamped = Math.max(0, Math.min(horizontalPart.thickness, offset));
  return side === 'right'
    ? Math.max(0, Math.min(horizontalPart.thickness, horizontalPart.thickness - clamped))
    : clamped;
}

function getSupportCenterX(horizontalPart: Part, supportPart: Part) {
  return supportPart.position.x - horizontalPart.position.x + horizontalPart.width / 2;
}

function getHorizontalCenterY(horizontalPart: Part) {
  return horizontalPart.height / 2;
}

function getVerticalLocalY(horizontalPart: Part, supportPart: Part) {
  return supportPart.height / 2 - (horizontalPart.position.y - supportPart.position.y);
}

function getTopSupportLocalX(horizontalPart: Part, support: SectionSupport, offset: number) {
  return support.supportFace === 'top' ? support.supportPart.width / 2 : offset;
}

function getSupportFaceLocalX(supportPart: Part, supportFace: PartFace, offset: number) {
  if (supportFace === 'left' || supportFace === 'right') {
    return Math.max(0, Math.min(supportPart.thickness, offset));
  }
  return Math.max(0, Math.min(supportPart.width, offset));
}

function getTopSupportLocalY(horizontalPart: Part, support: SectionSupport, offset: number) {
  return support.supportFace === 'top'
    ? offset
    : getVerticalLocalY(horizontalPart, support.supportPart);
}

function camFaceByPartRole(part: Part): PartFace {
  return part.meta?.role === 'bottom' ? 'top' : 'bottom';
}

function getCamCenterOffsetFromMountingEdge(width: number, sideKey: 'left' | 'right') {
  const clamped = Math.max(9, Math.min(width - 9, camCenterOffsetFromEdge));
  return sideKey === 'left' ? clamped : Math.max(9, width - clamped);
}

function getCamCenterOffsetFromTopEdge(height: number) {
  return Math.max(9, Math.min(height - 9, camCenterOffsetFromEdge));
}

function getCamCenterOffsetFromBottomEdge(height: number) {
  return Math.max(9, Math.min(height - 9, height - camCenterOffsetFromEdge));
}

function getPartitionTopCamFace(horizontalPart: Part, partition: Part): 'left' | 'right' {
  return partition.position.x <= horizontalPart.position.x ? 'right' : 'left';
}

function getPartitionBottomCamFace(horizontalPart: Part, partition: Part): 'left' | 'right' {
  return partition.position.x <= horizontalPart.position.x ? 'left' : 'right';
}

function getWidthThirdOffsets(width: number) {
  const clampX = (value: number) => Math.max(24, Math.min(width - 24, value));
  return [clampX(width / 3), clampX(width * 2 / 3)] as const;
}

function getBackPanelDistributedOffsets(span: number) {
  const clampOffset = (value: number) => Math.max(24, Math.min(span - 24, value));
  if (span > 1200) {
    return [clampOffset(span / 5), clampOffset(span / 2), clampOffset((span * 4) / 5)] as const;
  }
  return [clampOffset(span / 4), clampOffset((span * 3) / 4)] as const;
}

function getBackPanelTopJoinery(topJoinery: ReturnType<typeof createEmptySideJoinery>, overlayTop: boolean): JoineryType {
  if (!overlayTop && (topJoinery.left === 'minifix-dowel' || topJoinery.right === 'minifix-dowel')) return 'minifix-dowel';
  if (topJoinery.left === 'confirmat' || topJoinery.right === 'confirmat') return 'confirmat';
  return 'none';
}

function getBackPanelEdgeJoinery(backPanel: Part | null, edge: 'left' | 'right' | 'top' | 'bottom', fallback: JoineryType = 'none'): JoineryType {
  if (!backPanel) return 'none';
  const explicit = backPanel.meta?.joinery?.[edge];
  return explicit && explicit !== 'none' ? explicit : fallback;
}

