function isSectionBackPanel(sourceId?: string) {
  return typeof sourceId === 'string' && sourceId.startsWith('section-back-panel:');
}

function parseSectionBackPanelSource(sourceId?: string | null) {
  if (!isSectionBackPanel(sourceId ?? undefined)) return null;
  const [, , tierId, sectionId] = (sourceId ?? '').split(':');
  return tierId && sectionId ? { tierId, sectionId } : null;
}

function isPlinthEnabled(groupParts: Part[]) {
  return groupParts.some((part) => part.meta?.role === 'plinth-front' || part.meta?.role === 'plinth-back');
}

function isOverlayTop(top: Part, bottom: Part) {
  return top.width > bottom.width + 0.001 || top.thickness > bottom.thickness + 0.001;
}

